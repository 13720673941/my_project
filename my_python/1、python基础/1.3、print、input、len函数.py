# print 函数: 打印输出字符串

print('hello world!')

# print 传递参数

print("hello ",end='')

print("james")

print("hello","james",end=",")

# input 函数: 等待用户输入文本，并赋值给一个变量

name = input('what is your name?')

# 输入的字符串会赋值给变量name

print(name)

# len 函数: 统计传递的字符串的字符个数，输出是个整型

# 输出 len('hello') == 5

print(len('hello'))