# 指数运算符 **

print(2**3) # == 8

# 取余数/取模 %

print(22%8) # == 6

# 整除商取整数 //

print(22//8) # == 2

# 除法 /

print(22/8) # == 2.75

# 乘法 *

print(2*3) # == 6

# 加法 +

print(2+3) # == 5

# 减法 -

print(3-2) # == 1

# 运算优先级： ** 操作符首先求值，接下来是 * / // %从左到右，+ - 最后求值

# 如果需要可以通过括号来改变优先级

number = (2+3)*6

print(number) # number == 30

